    public void get_userDtls(){
        
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        Connection con;
        try{
            ///////////////////////////////
            ///////////////////////////////
            ///////////////////////////////
            try {
                Class.forName("oracle.jdbc.driver.OracleDriver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Register_Frm.class.getName()).log(Level.SEVERE, null, ex);
            }
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "ACCOUNTMAINTAINER", "SWARNAVA");
            ///////////////////////////////
            ///////////////////////////////
            ///////////////////////////////

            
            
            
            PreparedStatement ps = con.prepareStatement("SELECT * FROM USERS");
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                
                jTextField_propNm.setText(rs.getString("PROP_NAME"));
                jTextField_shopNm.setText(rs.getString("SHOPNAME"));
                jTextField_Phno.setText(rs.getString("PROP_PHNO"));
                jTextField_email.setText(rs.getString("PROP_EMAIL"));
                jTextArea_addr.setText(rs.getString("SHOP_ADDR"));
                jTextField_gst.setText(rs.getString("PROP_GST"));
                jTextField_pass.setText(rs.getString("PROP_PASS"));
                
            }
            
            
            

            ////////////////////////////////////////////////////////////////////
            ////////////////////////////////////////////////////////////////////
            ////////////////////////////////////////////////////////////////////
            con.close();
        }
        catch(Exception ea){
            ea.printStackTrace();
        }
        
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
