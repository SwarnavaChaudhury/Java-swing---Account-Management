    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        
        String PropNm = jTextField_propNm.getText();
        String ShopNm = jTextField_shopNm.getText();
        String Phno = jTextField_Phno.getText();
        String Email = jTextField_email.getText();
        String Addr = jTextArea_addr.getText();
        String gst = jTextField_gst.getText();
        String Pass = jTextField_pass.getText();
        
        
        if(PropNm.isEmpty() || ShopNm.isEmpty() || Phno.isEmpty() || Email.isEmpty() || Addr.isEmpty() || gst.isEmpty() || Pass.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Some Field is Empty!!", "Warning", JOptionPane.WARNING_MESSAGE);
            
        }else{
            
                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////
                Connection con;
                try{
                    ///////////////////////////////
                    ///////////////////////////////
                    ///////////////////////////////
                    try {
                        Class.forName("oracle.jdbc.driver.OracleDriver");
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(Register_Frm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "ACCOUNTMAINTAINER", "SWARNAVA");
                    ///////////////////////////////
                    ///////////////////////////////
                    ///////////////////////////////




                    
                    PreparedStatement ps = con.prepareStatement("UPDATE USERS SET PROP_NAME = ?, PROP_PHNO = ?, PROP_EMAIL = ?, SHOPNAME = ?, SHOP_ADDR = ?, PROP_GST = ?, PROP_PASS = ?");
                    ps.setString(1, PropNm);
                    ps.setString(2, Phno);
                    ps.setString(3, Email);
                    ps.setString(4, ShopNm);
                    ps.setString(5, Addr);
                    ps.setString(6, gst);
                    ps.setString(7, Pass);
                    int i = ps.executeUpdate();
                    if(i > 0){
                        ////////////////////////////////////////////////////////
                        JOptionPane.showMessageDialog(this, "Update Complete...", "Success", JOptionPane.INFORMATION_MESSAGE);
                        
                        ///////////////////////////////  show user updated details
                        get_userDtls();
                        
                        ////////////////////////////////////////////////////////
                    }else{
                        ////////////////////////////////////////////////////////
                        
                        ////////////////////////////////////////////////////////
                    }


                    
                    
                    
                    


                    ////////////////////////////////////////////////////////////////////
                    ////////////////////////////////////////////////////////////////////
                    ////////////////////////////////////////////////////////////////////
                    con.close();
                }
                catch(Exception ea){
                    ea.printStackTrace();
                }

                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////
            
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

