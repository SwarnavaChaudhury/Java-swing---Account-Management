    private void jButton_StoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_StoreActionPerformed
        
        ////////////////////////////////////////////////////////////////////////
        
        String Expenditure_type = jTextField_expn_type.getText();
        
        
        if(Expenditure_type.isEmpty()){
            JOptionPane.showMessageDialog(this, "Some Field is Empty!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{

                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////
                Connection con;
                try{
                    ///////////////////////////////
                    ///////////////////////////////
                    ///////////////////////////////
                    try {
                        Class.forName("oracle.jdbc.driver.OracleDriver");
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(Register_Frm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "ACCOUNTMAINTAINER", "SWARNAVA");
                    ///////////////////////////////
                    ///////////////////////////////
                    ///////////////////////////////

                    PreparedStatement sq = con.prepareStatement("SELECT * FROM EXPENDITURE WHERE EXPENDITURE_TYPE = ?");
                    sq.setString(1, Expenditure_type);
                    int ur = sq.executeUpdate();
                    if(ur > 0){
                        JOptionPane.showMessageDialog(this, "Duplicate Value Not Allow", "Warning", JOptionPane.WARNING_MESSAGE);
                    }else{
                        
                        PreparedStatement ps = con.prepareStatement("INSERT INTO EXPENDITURE(EXPENDITURE_TYPE) VALUES(?)");
                        ps.setString(1, jTextField_expn_type.getText());
                        int i = ps.executeUpdate();
                        if(i > 0){
                            JOptionPane.showMessageDialog(this, "Add Successfully...", "Status", JOptionPane.INFORMATION_MESSAGE);
                            jTextField_expn_type.setText("");
                            
                            ////////////////
                            ////////////////
                            DefaultTableModel tblmodel = (DefaultTableModel) jTable_Expenditure_Type.getModel();
                            tblmodel.setRowCount(0);
                            show_ExpnList();         ///////////////  for refresh table
                            ////////////////
                            ////////////////
                            
                        }else{
                            JOptionPane.showMessageDialog(this, "Error Occurred!!", "Status", JOptionPane.ERROR_MESSAGE);
                        }
                        
                    }
                    
                    
                    ////////////////////////////////////////////////////////////////////
                    ////////////////////////////////////////////////////////////////////
                    ////////////////////////////////////////////////////////////////////
                    con.close();
                }
                catch(Exception ea){
                    ea.printStackTrace();
                }

                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////
                ////////////////////////////////////////////////////////////////////////

        }
        
    }//GEN-LAST:event_jButton_StoreActionPerformed

    private void jButton_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeActionPerformed
        
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        Connection con;
        try{
            ///////////////////////////////
            ///////////////////////////////
            ///////////////////////////////
            try {
                Class.forName("oracle.jdbc.driver.OracleDriver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Register_Frm.class.getName()).log(Level.SEVERE, null, ex);
            }
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "ACCOUNTMAINTAINER", "SWARNAVA");
            ///////////////////////////////
            ///////////////////////////////
            ///////////////////////////////

            int il = jTable_Expenditure_Type.getSelectedRow();
            TableModel model = jTable_Expenditure_Type.getModel();
            String Expn_type = model.getValueAt(il, 0).toString();
            
            if(Expn_type.isEmpty()){
                JOptionPane.showMessageDialog(this, "Select any please!!", "Warning", JOptionPane.WARNING_MESSAGE);
            }else{
                PreparedStatement ps = con.prepareStatement("DELETE FROM EXPENDITURE WHERE EXPENDITURE_TYPE = ?");
                ps.setString(1, Expn_type);
                int io = ps.executeUpdate();
                if(io > 0){
                    JOptionPane.showMessageDialog(this, "Removed Successfully...", "Status", JOptionPane.INFORMATION_MESSAGE);
                    ////////////////
                    ////////////////
                    DefaultTableModel tblmodel = (DefaultTableModel) jTable_Expenditure_Type.getModel();
                    tblmodel.setRowCount(0);
                    show_ExpnList();         ///////////////  for refresh table
                    ////////////////
                    ////////////////
                }else{
                    JOptionPane.showMessageDialog(this, "Error Occurred!!", "Status", JOptionPane.ERROR_MESSAGE);
                }
            }

            ////////////////////////////////////////////////////////////////////
            ////////////////////////////////////////////////////////////////////
            ////////////////////////////////////////////////////////////////////
            con.close();
        }
        catch(Exception ea){
            ea.printStackTrace();
        }
        
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        ////////////////////////////////////////////////////////////////////////
        
    }//GEN-LAST:event_jButton_removeActionPerformed

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
