    public ArrayList<expndtr_type> ExpnList() {
        ArrayList<expndtr_type> ExpnList = new ArrayList<>();

        ///////////////////////////
        Connection con;
        try {
            ///////////////////////////////
            ///////////////////////////////
            ///////////////////////////////
            try {
                Class.forName("oracle.jdbc.driver.OracleDriver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Register_Frm.class.getName()).log(Level.SEVERE, null, ex);
            }
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "ACCOUNTMAINTAINER", "SWARNAVA");
            ///////////////////////////////
            ///////////////////////////////
            ///////////////////////////////

            expndtr_type expndtr_type;

            PreparedStatement ps = con.prepareStatement("SELECT * FROM EXPENDITURE");
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                expndtr_type = new expndtr_type(rs.getString("EXPENDITURE_TYPE"));
                ExpnList.add(expndtr_type);
            }
            
            /////////////////////////////////////////////
            /////////////////////////////////////////////
            /////////////////////////////////////////////
            con.close();
        } catch (Exception ea) {
            ea.printStackTrace();
        }
        ////////////////////////////////////////////////////////////////////////
        return ExpnList;
        ////////////////////////////////////////////////////////////////////////
    }
    
    
    
    public void show_ExpnList() {
        ArrayList<expndtr_type> expenditureLst = ExpnList();
        DefaultTableModel model = (DefaultTableModel) jTable_Expenditure_Type.getModel();
        Object[] row = new Object[1];
        for (int i = 0; i < expenditureLst.size(); i++) {
            row[0] = expenditureLst.get(i).getExpnditure_type();
            model.addRow(row);
        }
    }
    
    
